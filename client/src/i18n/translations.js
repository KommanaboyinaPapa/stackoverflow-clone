export const getNested = (obj, path) =>
  path.split('.').reduce((acc, key) => (acc && acc[key] !== undefined ? acc[key] : null), obj);

export const interpolate = (str, vars = {}) =>
  typeof str === 'string'
    ? str.replace(/\{\{(\w+)\}\}/g, (_, k) => (vars[k] !== undefined ? vars[k] : ''))
    : str;
